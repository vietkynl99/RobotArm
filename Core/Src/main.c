void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  if (huart->Instance == USART1)
  {
    uartBuffWIndex++;
    if (uartBuffWIndex >= UART_BUFFER_SIZE)
    {
      uartBuffWIndex = 0;
    }
    uartRxBuffer[uartBuffWIndex] = uartRxData;
    HAL_UART_Receive_IT(&huart1, &uartRxData, 1);
  }
}

